public class RedheadDuck extends Duck implements Quackable, Flyable{
    public void display(){
        System.out.println("I'm a Redhead Duck");
    }
    public void swim(){
        System.out.println("I, the Redhead Duck, am swimming");
    }
    public void quack() {
        System.out.println("I, the Redhead Duck, said quack, quack");
    }
    public void fly() {
        System.out.println("I, the Redhead Duck, fly");
    }
}
